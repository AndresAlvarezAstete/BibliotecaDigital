package Biblioteca;

public class Libros
{
    private int Farenheit;
    private int Revival;
    private int ElAlquimista;

    public Libros()
    {
        Farenheit = 7000;
        Revival = 22000;
        ElAlquimista = 45000;
    }

    public int getFarenheit()
    {
        return Farenheit;
    }

    public int getRevival()
    {
        return Revival;
    }

    public int getElAlquimista()
    {
        return ElAlquimista;
    }

}
